package com.khadri.spring.mvc.calc.service;

public class CalcService {

	public Integer addition(Integer v1, Integer v2) {
		return v1 + v2;
	}
	
	
	public Integer substraction(Integer v1, Integer v2) {
		return v1 - v2;
	}
	
	public Integer multiplication(Integer v1, Integer v2) {
		return v1 * v2;
	}
}
